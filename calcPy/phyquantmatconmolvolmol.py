'''
Physique : Quantité de matière, concentration molaire et volume molaire

Mole = unité officielle de la quantité de matière
  constante égale à 6.02*10p23

n = quantité de matière mesurée en mol
N = nombre d'entitées présentes dans l'échantillon
Na = constante d'avogadro égale à 6.02214*10p23 mol-1

n = N/Na

Masse molaire atomique = masse d'une mole d'atomes de ce dit élément chimique, mesurée alors en g/mol-1

Masse molaire moléculaire = somme des masses molaires atomiques des éléments chimiques constituant la molécule. Mesurée en g.mol-1
EXEMPLE:
Masse molaire atomique de l'oxygène O = 16.0 g.mol-1
M(H2O) = M(H)*2 + M(O) = 2*1.00 + 16 = 18 g.mol-1

M = masse molaire de l'échantillon (g.mol-1)
m = masse de l'échantillon (g)
n = quantité de matière de l'échantillon (mol)

/////////////
n = m/M
n = (p*V)/M

n en mol
p en g.mol-1
V en mL
////////////

Vm = Volume occupé par une mole de gaz. Mesuré en L/mol.
Indépendant de la nature du gaz, mais dépendant de la température et de la pression.
//////////////
n = V/Vm

V en L
/////////////

Concentration molaire = quantité de matière de soluté présente par litre de solution.

//////////////
Cm = C * M

Cm (g.L-1)
C (mol.L-1)
M (g.mol-1)
//////////////

Concentration massique = pour une espèce en solution, correspond à la masse de soluté présente par litre de solution.
Notée Cm, exprimée en g-L-1

//////////////
Cm = m(espèce)/Vsolution

Cm (g.L-1)
m (g)
V (L)
//////////////

LA BOITE A FORMULES
+++++++++++++++++++++++++
n = m / M = p*V/M = N/Na = C*V = Vg/Vm

Cm = m/v (g/L)

C = m/M/V * 1/V = Cm/M
+++++++++++++++++++++++++
'''
